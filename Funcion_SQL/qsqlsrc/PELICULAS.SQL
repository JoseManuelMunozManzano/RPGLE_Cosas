CREATE OR REPLACE TABLE JOMUMA1.PELICULAS (
  NOMBRE CHAR(150) CCSID 1145 NOT NULL DEFAULT '',
  ANIO DECIMAL(4,0) NOT NULL WITH DEFAULT, 
  CONSTRAINT JOMUMA1.PELICULASK1 PRIMARY KEY(NOMBRE)
) RCDFMT RPELIS;

LABEL ON TABLE JOMUMA1.PELICULAS IS 'PELICULAS Y AÑO DE ESTRENO';

LABEL ON COLUMN JOMUMA1.PELICULAS (
  NOMBRE IS 'NOMBRE DE PELICULA',
  ANIO IS 'AÑO DE ESTRENO'
);

LABEL ON COLUMN JOMUMA1.PELICULAS (
  NOMBRE TEXT IS 'NOMBRE DE PELICULA',
  ANIO TEXT IS 'AÑO DE ESTRENO'
);

